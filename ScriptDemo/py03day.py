#====================== range() 函数 ======================================================
# 函数说明：该函数可生成一个等差数列
# range(x) 生成一个长度为x得等差数列
# range(x,y) 生成一个步长为y的长度为x的等差数列,步长可以为负数
# range(x,y,z) 生成一个以x开头的z结尾的长度为x的等差数列，步长可以为负数

# range()函数的使用：

for i in range(len(lists)):
    print('第%d个元素是%s' %(i, lists[i]))

# xrange 和 range 的区别
# xrange 效率比range高，因为xrange不生成完整的列表
# xrange 只在for循环中使用才有意义
#===========================================================================================

#=============== 循环终止语句==========================================================================

# break 跳出循环，执行下一段代码

# 代码参考 while循环

# continue 跳出当前循环，继续下一次循环

for item in lists:
    if item == 'a':
        continue 
    print(item)

# pass 空语句占位符，用于保持结构完整性

for item in lists:
    if item == 'a':
        pass
        print('这个家伙叫pass')
    else:
        print(item)

