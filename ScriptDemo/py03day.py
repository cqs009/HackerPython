#===================== while 循环 ==================================================

lists = []

count = 0
while(count <5):
    lists.append(input('lists添加元素：'))
    if 'y' in lists:
        break
    count = count + 1

print(lists)

#========================================================================================

#===================== for 循环 ==========================================================

count = 0
for word in lists:
    print('item=='+word)

# 迭代过程中修改迭代序列是不安全的，可使用切片复制
for word in lists[:]:
    if len(word) > 0:
        lists.insert(0,word)
    else:
        lists.pop()

print(lists)

