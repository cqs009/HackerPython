#============================= 集合 =========================================================================================

# 集合由多种不同的元素组成是一个不可重复的无序的元素序列，Python提供了两种集合：可变集合和不可变集合

# 定义集合 可使用 大括号{}和set()

# 空集合 使用 set() ; {}:用于定义空字典

nullset = set()

set1 = {"a",1,'b'}


set2 = set('12345678avbd')

# 集合的遍历；由于集合是无序的，所以不可以像列表那样创建索引或切片操作，只能使用for循环
# 或者使用 in/not in来访问和判断集合元素

print("a" in set1)

print("c" not in set2)


for item in set2:
    print(item)

# 集合元素的添加
set2.add("xxxxxx")
print(set2)
 
 # 集合元素的修改
set2.update([3,2])
print(set2)

# 集合元素的移除
set2.remove("a")
print(set2)

# 求并集
nullset = nullset.union(set1,set2)
print(nullset)

