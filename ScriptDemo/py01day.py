#===================================================================================
#============================= 函数扩展  ==============================================

# 数学函数 需要引入 math 模块 数学常量 pi（圆周率） e (自然常数)
from math import * 
# ceil(x) 该函数返回数字的上入整数
print(ceil(4.2))

# floor(x) 该函数返回数字的下舍整数
print(floor(4.9))

# fabs(x) 该函数返回数字的绝对值
print(fabs(-23))

# factorial(x) 该函数返回数字的阶乘
print(factorial(5))

# hypot(x,y) 该函数返回欧几里得范数 sqrt(x*x+y*y) 勾三股四弦五
print(hypot(3,4))

# sqrt(x) 该函数返回数字的平方根
print(sqrt(25))

# pow(x,y) 该函数返回x的y次方 x**y
print(pow(2, 10))

# log(x,y) 该函数返回以y为基数的x的对数
print(log(25,5))

# log10(x) 该函数返回以10为基数的x的对数
print(log10(1000))

# trunc(x) 该函数返回数字的整数部分
print(trunc(2.333333))

# isnan(x) 判断对象或集合是否为空
# 创建一个对象 
c = float('nan')
b = bool(nan)
print(isnan(c))
print(isnan(b))

class Zero:pass
c = Zero
print(str(bool(c)))

# degrees(x) 将弧度转换成角度  
print(degrees(pi/4))

# radians(x) 将角度转换为弧度
print(radians(60))

